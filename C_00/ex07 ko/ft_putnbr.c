#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	v;
	int	a[10];

	v = '0';
	if (nb < 0)
	{
		nb = nb*(-1);
		write(1, "-", 1);
	}
	if (nb == 0)
		write(1, "0", 1);
	else
	{
		while (nb > 0)
		{
			a[v] = (nb % 10) + '0';
			nb = nb / 10;
			v++;
		}
		while (v >= 0)
		{	
			v--;
			write(1, &a[v], 1);
		}
	}
}
		
